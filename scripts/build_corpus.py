def seed_sentences() -> list[str]:
    """All hand-written seed lines, deduplicated, order-stable."""
    groups = [
        SENTENCES_EVERYDAY,
        SENTENCES_NEWS,
        SENTENCES_NAMES,
        SENTENCES_VERSE,
        SENTENCES_RELIGIOUS_FORMAL,
        SENTENCES_SCHOOL,
        SENTENCES_GREETINGS,
        SENTENCES_ADDRESSES,
        SENTENCES_MIXED,
        SENTENCES_HOME_FORMS,
        SENTENCES_RARE_GRAPHEMES,
    ]
    seen, out = set(), []
    for group in groups:
        for line in group:
            line = " ".join(line.split())
            if line and line not in seen:
                seen.add(line)
                out.append(line)
    return out


def expand_templates(rng: random.Random, count: int) -> list[str]:
    """Deterministic template expansion producing grammatical short sentences."""
    out = []
    for _ in range(count):
        template = rng.choice(TEMPLATES)
        line = template.format(
            subject=rng.choice(SUBJECTS),
            verb=rng.choice(VERBS_PAST),
            place=rng.choice(PLACES),
            time=rng.choice(TIMES),
            object=rng.choice(OBJECTS),
        )
        out.append(" ".join(line.split()))
    return out


def sentence_spans(rng: random.Random, sentences: list[str], count: int) -> list[str]:
    """Random contiguous word spans (1-6 words) cut from long seed sentences."""
    long_sentences = [s for s in sentences if len(s.split()) >= 4]
    out = []
    for _ in range(count):
        words = rng.choice(long_sentences).split()
        span = rng.randint(1, min(6, len(words)))
        start = rng.randint(0, len(words) - span)
        out.append(" ".join(words[start:start + span]))
    return out


def word_recombinations(rng: random.Random, count: int) -> list[str]:
    """Shuffle words from the legacy word list into 2-8 word pseudo-lines."""
    words_path = os.path.join(ROOT, "src", "data", "sample_words.txt")
    vocab_path = os.path.join(ROOT, "src", "data", "form_vocab.txt")
    words = []
    for path in (words_path, vocab_path):
        if os.path.isfile(path):
            with open(path, "r", encoding="utf-8") as f:
                words.extend(w.strip() for w in f
                             if w.strip() and not w.lstrip().startswith("#"))
    if not words:
        return []
    out = []
    for _ in range(count):
        n = rng.randint(2, 8)
        out.append(" ".join(rng.choice(words) for _ in range(n)))
    return out


def numeric_lines(rng: random.Random, count: int) -> list[str]:
    """Dates, amounts, phone numbers and labelled numeric fields."""
    labels = [
        "දිනය", "මුදල", "දුරකථන අංකය", "ලියාපදිංචි අංකය", "අංකය",
        "වයස අවුරුදු", "මිල රුපියල්", "ශ්‍රේණිය", "කාමර අංකය", "පිටු අංකය",
    ]
    out = []
    for _ in range(count):
        kind = rng.random()
        if kind < 0.3:
            y, m, d = rng.randint(1990, 2026), rng.randint(1, 12), rng.randint(1, 28)
            out.append(rng.choice([f"{y}.{m:02d}.{d:02d}", f"{y}-{m:02d}-{d:02d}", f"{d:02d}/{m:02d}/{y}"]))
        elif kind < 0.55:
            out.append(f"{rng.choice(labels)} {rng.randint(1, 99999)}")
        elif kind < 0.75:
            out.append(f"රුපියල් {rng.randint(10, 99999)}.{rng.randint(0, 99):02d} කි")
        elif kind < 0.9:
            out.append(f"0{rng.randint(70, 77)}{rng.randint(1000000, 9999999)}")
        else:
            out.append(f"{rng.randint(1, 13)}-{rng.choice('ABCDEFG')}")
    return out


def build_corpus(target_min: int = TARGET_MIN_LINES, seed: int = SEED) -> list[str]:
    rng = random.Random(seed)
    seeds = seed_sentences()
    lines = list(seeds)
    lines += expand_templates(rng, 1400)
    lines += sentence_spans(rng, seeds, 900)
    lines += word_recombinations(rng, 500)
    lines += numeric_lines(rng, 250)

    seen, out = set(), []
    for line in lines:
        if line not in seen:
            seen.add(line)
            out.append(line)

    # Top up with more template expansions if dedup dropped us below target.
    attempts = 0
    while len(out) < target_min and attempts < 50:
        for line in expand_templates(rng, 500):
            if line not in seen:
                seen.add(line)
                out.append(line)
        attempts += 1

    rng.shuffle(out)
    return out


def check_charset_coverage(lines: list[str]) -> list[str]:
    """Return sorted list of characters used in ``lines`` missing from the charset."""
    from src.charset import Charset

    cs = Charset.build_default()
    used = set("".join(lines))
    missing = sorted(c for c in used if c not in cs.char_to_idx and c != "\n")
    return missing


def main() -> None:
    out_path = os.path.join(ROOT, "src", "data", "corpus_sinhala.txt")
    lines = build_corpus()
    missing = check_charset_coverage(lines)
    if missing:
        print(f"WARNING: {len(missing)} corpus characters missing from charset:")
        for c in missing:
            print(f"  U+{ord(c):04X} {c!r}")
    with open(out_path, "w", encoding="utf-8", newline="\n") as f:
        f.write("\n".join(lines) + "\n")
    n_seed = len(seed_sentences())
    zwj = sum(line.count("\u200d") for line in lines)
    print(f"wrote {len(lines)} lines ({n_seed} hand-written seeds) -> {out_path}")
    print(f"ZWJ occurrences: {zwj}")


if __name__ == "__main__":
    main()
